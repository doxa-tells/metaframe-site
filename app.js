function applyLang(lang) {
    if (!T[lang]) lang = "ru";
    const dict = T[lang];
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const k = el.getAttribute("data-i18n");
        if (dict[k] !== undefined) el.textContent = dict[k];
    });
    document.querySelectorAll("[data-i18n-html]").forEach(el => {
        const k = el.getAttribute("data-i18n-html");
        if (dict[k] !== undefined) el.innerHTML = dict[k];
    });
    document.documentElement.lang = dict._meta.htmlLang;
    document.title = dict._meta.title;
    document.querySelectorAll(".lang-btn").forEach(b => {
        b.classList.toggle("active", b.dataset.lang === lang);
    });
    window._recCity = dict._meta.recCity;
    try { localStorage.setItem("metaframe_lang", lang); } catch (e) {}
}

(function initLang() {
    let saved = null;
    try { saved = localStorage.getItem("metaframe_lang"); } catch (e) {}
    let lang = saved;
    if (!lang) {
        const nav = (navigator.language || "ru").slice(0, 2).toLowerCase();
        lang = T[nav] ? nav : "ru";
    }
    applyLang(lang);
    document.querySelectorAll(".lang-btn").forEach(b => {
        b.addEventListener("click", () => applyLang(b.dataset.lang));
    });
})();

